--JUNTAR TABELAS E MANTER CLIENTES SEM E-MAIL
SELECT 
	TC.ID_CLIENTE,
	TC.NOME,
	TE.EMAIL
FROM
	TB_CLIENTE2 TC
	LEFT JOIN TB_EMAIL2 TE ON TC.ID_CLIENTE = TE.ID_CLIENTE


