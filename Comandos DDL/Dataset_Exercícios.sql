	ID_CLIENTE INT NOT NULL
)
GO
ALTER TABLE TB_DATA_CADASTRO ADD CONSTRAINT PK_DATA_CADASTRO PRIMARY KEY (ID_DATA_CADASTRO)
GO
ALTER TABLE TB_DATA_CADASTRO ADD CONSTRAINT UNIQ_CLIENTE_DATA_CADASTRO UNIQUE (ID_CLIENTE)
GO

