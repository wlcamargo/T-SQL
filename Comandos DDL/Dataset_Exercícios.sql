--DATASET EXERCÍCIOS

DROP DATABASE IF EXISTS DATASET_EXERCICIOS
GO
CREATE DATABASE DATASET_EXERCICIOS
GO

USE DATASET_EXERCICIOS
GO

--CRIAR TABELA DE CLIENTES
CREATE TABLE TB_CLIENTE (
	ID_CLIENTE INT IDENTITY(1,1),
	NOME VARCHAR(30) NOT NULL,
)
ALTER TABLE TB_CLIENTE ADD CONSTRAINT PK_CLIENTE PRIMARY KEY (ID_CLIENTE)
GO

--============================================================================

--CRIAR TABELA DE CONTATOS
CREATE TABLE TB_TELEFONE (
	ID_TELEFONE INT IDENTITY (1,1),
	TELEFONE VARCHAR (15),
	ID_CLIENTE INT NOT NULL
)

ALTER TABLE TB_TELEFONE ADD CONSTRAINT PK_TELEFONE PRIMARY KEY (ID_TELEFONE)
GO
ALTER TABLE TB_TELEFONE ADD CONSTRAINT FK_CLIENTE_TELEFONE FOREIGN KEY (ID_CLIENTE) REFERENCES TB_CLIENTE (ID_CLIENTE)
GO

--============================================================================

--CRIAR TABELA DE DOCUMENTOS
CREATE TABLE TB_DOCUMENTOS (
	ID_DOCUMENTO INT IDENTITY (1,1),
	CPF VARCHAR (10) NOT NULL,
	ID_CLIENTE INT NOT NULL
)

ALTER TABLE TB_DOCUMENTOS ADD CONSTRAINT PK_DOCUMENTO PRIMARY KEY (ID_DOCUMENTO)
GO
ALTER TABLE TB_DOCUMENTOS ADD CONSTRAINT FK_CLIENTE_DOCUMENTO FOREIGN KEY (ID_CLIENTE) REFERENCES TB_CLIENTE (ID_CLIENTE)
GO
ALTER TABLE TB_DOCUMENTOS ADD CONSTRAINT UNIQ_CLIENTE UNIQUE (ID_CLIENTE) 
GO
--============================================================================

--CRIAR TABELA DE EMAIL
CREATE TABLE TB_EMAIL (
	ID_EMAIL INT IDENTITY (1,1),
	EMAIL VARCHAR (100),
	ID_CLIENTE INT NOT NULL
)
GO
ALTER TABLE TB_EMAIL ADD CONSTRAINT PK_EMAIL PRIMARY KEY (ID_EMAIL)
GO
ALTER TABLE TB_EMAIL ADD CONSTRAINT FK_CLIENTE_EMAIL FOREIGN KEY (ID_CLIENTE) REFERENCES TB_CLIENTE (ID_CLIENTE)
GO

--============================================================================

--CRIAR TABELA SEXO
CREATE TABLE TB_SEXO (
	ID_SEXO INT IDENTITY (1,1),
	SEXO CHAR(1),
	ID_CLIENTE INT NOT NULL
)
GO
ALTER TABLE TB_SEXO ADD CONSTRAINT PK_CLIENTE_SEXO PRIMARY KEY (ID_SEXO)
GO
ALTER TABLE TB_SEXO ADD CONSTRAINT UNIQ_CLIENTE_SEXO UNIQUE (ID_CLIENTE)
GO

--============================================================================
--CRIAR TABELA PAIS
CREATE TABLE TB_PAIS (
	ID_PAIS INT IDENTITY (1,1),
	PAIS VARCHAR(20) NOT NULL,
	ID_CLIENTE INT NOT NULL
)
GO
ALTER TABLE TB_PAIS ADD CONSTRAINT FK_CLIENTE_PAIS PRIMARY KEY (ID_PAIS)
GO
ALTER TABLE TB_PAIS  ADD CONSTRAINT UNIQ_CLIENTE_PAIS UNIQUE (ID_CLIENTE)
GO

--============================================================================
--CRIAR TABELA NASCIMENTO
CREATE TABLE TB_NASCIMENTO (
	ID_NASCIMENTO INT IDENTITY (1,1),
	DATA_NASCIMENTO DATE NOT NULL,
	ID_CLIENTE INT NOT NULL
)
GO
ALTER TABLE TB_NASCIMENTO ADD CONSTRAINT FK_CLIENTE_NASCIMENTO PRIMARY KEY (ID_NASCIMENTO)
GO
ALTER TABLE TB_NASCIMENTO ADD CONSTRAINT UNIQ_CLIENTE_NASCIMENTO UNIQUE (ID_CLIENTE)
GO

--============================================================================
--CRIAR TABELA DATA DO CADASTRO
CREATE TABLE TB_DATA_CADASTRO (
	ID_DATA_CADASTRO INT IDENTITY (1,1),
